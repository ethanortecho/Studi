/**
 * Production-ready logging utility
 * Automatically disables console logs in production builds
 */

const isDevelopment = __DEV__;

export const logger = {
  log: (...args: any[]) => {
    if (isDevelopment) {
      console.log(...args);
    }
  },
  
  warn: (...args: any[]) => {
    if (isDevelopment) {
      console.warn(...args);
    }
  },
  
  error: (...args: any[]) => {
    // Always log errors, even in production (for crash reporting)
    console.error(...args);
  },
  
  debug: (...args: any[]) => {
    if (isDevelopment) {
      console.debug(...args);
    }
  },
  
  info: (...args: any[]) => {
    if (isDevelopment) {
      console.info(...args);
    }
  }
};

// Environment logging
if (isDevelopment) {
  logger.info('ğŸ› Development logging enabled');
} else {
  // Only errors will be logged in production
  console.log('ğŸ”’ Production mode - debug logging disabled');
}